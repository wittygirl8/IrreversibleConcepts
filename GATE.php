<p>&nbsp;</p></div><div style='margin-top:20px' id='34_GATE'><h1>Why GATE?</h1>

<p>&nbsp;</p>

<p>GATE today has opened new avenues for budding professionals. Gives them an opportunity to be a part of prestigious public sector undertaking (PSUs) and reputed institutes and research organizations which are renowned for their excellence.</p>


<p>&nbsp;</p>

<p><strong>These are the reasons we strongly recommend an engineer to appear for GATE exam:

</strong></p>

<ul>
	<li>1. Several reputed PSUs recruits on basis of GATE Score.</li>
<li>2. M. Tech Admissions at IITs/ISM/IISc</li>
<li>3. Teaching: Professor, Asst. Professor at IITs, NITs, reputed educational institutes etc.</li>
<li>4. Makes you eligible for Junior Research Fellow and Senior Research Fellow on UGC and MHRD projects</li>
<li>5.Candidates on qualifying GATE, get a financial aid in the form of UGC scholarship for pursuing higher studies</li>
<li>6. Gives you an opportunity to study abroad: The institutes offering Masters for GATE qualifiers are National University of Singapore (NUS), Singapore.
<li>7. Career in Research & Development projects</li>
<li>8. Technical value addition to resume.</li>
<li>9. Expertise in subject/domain specialization.</li>

</ul></div><div style='margin-top:20px' id='35_GATE'><h1>GATE 2019&nbsp;Eligibility</h1>

<p>&nbsp;</p>

<p>The following candidates are eligible to appear in GATE 2018:</p>

<ul>
	<li>Bachelor&rsquo;s degree holders in Engineering/Technology (4 years after 10+2 or 3 years after B.Sc./Diploma in Engineering/ Technology) and those who are in the final year of such programs.</li>
	<li>Bachelor&rsquo;s degree holders in Architecture (Five years course) and those who are in the final year of such programs.</li>
	<li>Bachelor&rsquo;s degree holders of Four-year program in Science (B.S.) and those who are in the final year of such programs.</li>
	<li>Master&rsquo;s degree holders in any branch of Science/ Mathematics/ Statistics/Computer Applications or equivalent and those who are in the final year of such programs.</li>
	<li>Holders of Four-year Integrated Master&rsquo;s degree (Post B.Sc.) in Engineering/Technology and those who are in the second or higher year of such programs.</li>
	<li>Holders of Five-year Integrated Master&rsquo;s degree or Dual Degree in Engineering/Technology and those who are in the fourth or higher year of such programs.</li>
	<li>Holders of Five-year integrated M.Sc. or Five-year integrated B.Sc./M.Sc. degree and those who are in the final year of such programs.</li>
	<li>Candidates with qualifications obtained through examinations conducted by professional societies recognized by UPSC/AICTE/ MHRD as equivalent to B.E./B.Tech. Those who have completed Section A of AMIE or equivalent of such professional courses are also eligible.</li>
