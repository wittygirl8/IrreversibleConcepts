</ul></div><div style='margin-top:20px' id='41_GATE'><h1>Types of Questions</h1>

<p>&nbsp;</p>

<p style="text-align:justify"><strong>GATE 2018&nbsp;Exam</strong> consists of <strong>Multiple Choice Questions</strong> and <strong>Numerical Type Questions </strong> in all papers &amp; sections. <strong>Multiple Choice Questions</strong> will contain 4 options, of which only one is correct. For <strong>Numerical Type Questions </strong> a numerical value&nbsp;as the answer should be entered using mouse and virtual keyboard on the monitor.&nbsp;The following are type of Questions:</p>

<p style="text-align:justify">&nbsp;</p>

<p style="text-align:justify"><span style="font-size:14px"><strong>a. Recall:&nbsp;</strong></span>These are based on facts, principles, formulae or laws of the discipline. The candidate is expected to be able to obtain the answer either from his/her memory directly or at most from a one-line computation.</p>

<p style="text-align:justify"><span style="font-size:14px"><strong>b. Comprehension:&nbsp;</strong></span>These questions will test the candidate&#39;s understanding of the basics of his/her field, by requiring him/her to draw simple conclusions from fundamental ideas.</p>

<p style="text-align:justify"><span style="font-size:14px"><strong>c. Application:&nbsp;</strong></span>In these questions, the candidate is expected to apply his/her knowledge either through computation or by logical reasoning.</p>

<p style="text-align:justify"><span style="font-size:14px"><strong>d. Analysis and Synthesis:&nbsp;</strong></span>These can be linked questions, where the answer to the first question of the pair is required in order to answer its successor. Or these can be common data questions, in which two questions share the same data but can be solved independently of one another.</p>

<p style="text-align:justify"><span style="font-size:14px"><strong>e. Common Data Questions:&nbsp;</strong></span>Multiple questions may be linked to a common data problem, passage and the like. Two or three questions can be formed from the given common data problem. Each question is independent and its solution obtainable from the above problem data/passage directly. (Answer of the previous question is not required to solve the next question). Each question under this group will carry two marks.</p>

<p style="text-align:justify"><span style="font-size:14px"><strong>f. Linked Answer Questions:&nbsp;</strong></span>These questions are of problem solving type. A problem statement is followed by two questions based on the problem statement. The two questions are designed such that the solution to the second question depends upon the answer to the first one. In other words, the first answer is an intermediate step in working out the second answer. Each question in such linked answer questions will carry two marks.</p></div><div style='margin-top:20px' id='43_GATE'><h1>How to prepare for GATE&nbsp;2019?</h1>

