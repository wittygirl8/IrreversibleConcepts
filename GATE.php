                    <div style='margin-top:20px' id='33_GATE'><h1>What is GATE?</h1>

<p>&nbsp;</p>

<p style="text-align:justify">Graduate Aptitude Test in Engineering (GATE) is an all India entrance examination that primarily tests the comprehensive understanding of various undergraduate subjects in engineering and science. GATE is administered and conducted jointly by Indian Institute of Science-IISc and Indian Institute of Technology-IITs (IIT-Bombay, IIT-Delhi, IIT-Guwahati, IIT-Kanpur, IIT-Kharagpur, IIT-Madras and IIT-Roorkee), every year in the month of January/February on behalf of the National Coordination Board-GATE Dept. of Higher Education, Ministry of Human Recourses Development (MHRD) and Government of India.</p> <p style="text-align:justify">The GATE score of a candidate reflects the relative performance level of a candidate. The GATE score has now became an essential eligibility criteria for getting admission into the premium institutes for post-graduate education programs. Candidates on qualifying GATE, get a financial assistance of Rs 12,400/- (for Master's) and 25000/- (for Doctorate) in the form of UGC or MHRD scholarship. Recently, GATE Score has also become a criteria by several PSUs for recruiting young engineers in entry-level positions with attractive CTC.</p>

